class Solution:
    def maxAreaOfIsland(self, grid: List[List[int]]) -> int:
        if not grid:  # å¦‚æœä¸ºç©ºï¼Œåˆ™è¿”å›0
            return 0
        m = len(grid)
        n = len(grid[0])
        area = 0
        for i in range(m):
            for j in range(n):
                if grid[i][j]:  # å¦‚æœgrid[i][j]=1ï¼Œåˆ™æ±‚æ­¤å—å²›å±¿çš„é¢ç§¯
                    area = max(area, self.dfs(grid, i, j))
