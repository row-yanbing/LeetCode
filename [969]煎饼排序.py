class Solution:
    def pancakeSort(self, arr: List[int]) -> List[int]:
        if not arr or len(arr) == 1:
            return []
        max_index = arr.index(max(arr)) #æ‰¾åˆ°åºåˆ—ä¸­çš„æœ€å¤§å€¼
        res = []
        if max_index != len(arr)-1: #è‹¥æœ€å¤§å€¼åœ¨åºåˆ—ç»“å°¾ï¼Œåˆ™ä¸ç”¨åè½¬
            arr = arr[:max_index+1][::-1] + arr[max_index+1:] #å°†æœ€å¤§å€¼åè½¬åˆ°åºåˆ—æœ€å‰é¢
            arr = arr[::-1]#å°†æœ€å¤§å€¼åè½¬è‡³åºåˆ—å°¾éƒ¨
            res = res + [max_index+1, len(arr)] #å°†åè½¬è·¯å¾„æ·»åŠ è‡³ç»“æœ
        tmp = self.pancakeSort(arr[:-1]) #é€’å½’è°ƒç”¨
        res = res + tmp
        return res

